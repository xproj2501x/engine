let instance = null;

const LEVEL = {
  LOG: 'LOG',
  WARN: 'WARN',
  INFO: 'INFO',
  ERROR: 'ERROR'
};

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * LogService
 * @class
 * @memberof module:logService
 */
class Log {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * @private
   * @type { Array }
   */
  _log = [];

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  constructor() {
    if (!instance) {
      instance = this;
    }
    return instance;
  }

  write(msg) {
    this._log.push(msg);
    console.log(msg);
  }
  ////////////////////////////////////////////////////////////////////////////////
  // Public Methods
  ////////////////////////////////////////////////////////////////////////////////

  clear() {
    this._log = [];
  }

  ////////////////////////////////////////////////////////////////////////////////
  // Private Methods
  ////////////////////////////////////////////////////////////////////////////////

}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Log;
